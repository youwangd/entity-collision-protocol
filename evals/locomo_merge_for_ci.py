"""Merge separate LoCoMo per-vw result JSONs into the schema expected by
`evals.bootstrap_ci` (rows with baseline_per_query + bm25_only_per_query).

The convention: the cell with vector_weight == 0.0 is the BM25-only baseline.
Every other cell becomes a "baseline" row paired against that bm25 cell.

Per-query records are matched positionally — they must come from the same
ingestion order, which is the case if all cells were generated by
`run_locomo` on the same dataset+max_instances+k.

Usage:
    python -m evals.locomo_merge_for_ci \
        --in bench/results/locomo10_st_vw0.0.json \
        --in bench/results/locomo10_st_vw0.3.json \
        --in bench/results/locomo10_st_vw0.5.json \
        --in bench/results/locomo10_st_vw0.7.json \
        --in bench/results/locomo10_st_vw1.0.json \
        --out bench/results/locomo10_st_sweep.json
"""
from __future__ import annotations

import argparse
import json
from pathlib import Path
from evals.io_utils import atomic_write_json


def main():
    p = argparse.ArgumentParser()
    p.add_argument("--in", dest="inputs", action="append", required=True)
    p.add_argument("--out", required=True)
    p.add_argument("--baseline-vw", type=float, default=0.0,
                   help="vector_weight value to treat as BM25-only baseline")
    args = p.parse_args()

    cells = [json.loads(Path(x).read_text()) for x in args.inputs]
    bm25 = next((c for c in cells if (c.get("vector_weight") or 0.0) == args.baseline_vw), None)
    if bm25 is None:
        raise SystemExit(f"no cell with vector_weight={args.baseline_vw}")
    bm25_pq = bm25.get("per_query") or []

    rows = []
    for c in cells:
        vw = c.get("vector_weight") or 0.0
        pq = c.get("per_query") or []
        row = {
            "vector_weight": vw,
            "embedder": c.get("embedder"),
            "n_questions_scored": c.get("n_questions_scored"),
            "session_hit_at_1": c.get("session_hit_at_1"),
            "session_hit_at_k": c.get("session_hit_at_k"),
            "baseline_per_query": pq,
            "bm25_only_per_query": bm25_pq,
        }
        rows.append(row)

    out = {
        "config": {
            "source": "locomo_merge_for_ci",
            "embedder": bm25.get("embedder"),
            "k": bm25.get("k"),
            "n_samples": bm25.get("n_samples"),
            "n_questions_scored": bm25.get("n_questions_scored"),
            "baseline_vw": args.baseline_vw,
        },
        "sweep": rows,
    }
    Path(args.out).parent.mkdir(parents=True, exist_ok=True)
    atomic_write_json(args.out, out)
    print(f"[locomo_merge] {len(rows)} rows -> {args.out}")


if __name__ == "__main__":
    main()
